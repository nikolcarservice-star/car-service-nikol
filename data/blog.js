export function getAllBlogSlugs() {
  return blogPosts.map((p) => p.slug);
}

export function getBlogPost(slug, lang) {
  const post = blogPosts.find((p) => p.slug === slug);
  if (!post) return null;
  return {
    slug: post.slug,
    title: lang === 'ru' ? post.titleRu : post.titlePl,
    excerpt: lang === 'ru' ? post.excerptRu : post.excerptPl,
    body: lang === 'ru' ? (post.bodyRu || []) : (post.bodyPl || []),
  };
}
